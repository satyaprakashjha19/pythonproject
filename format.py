# format method

#integer
print("{}".format(10))
print("{} {}".format(10,20))
print("{}      {}".format(10,20))

print("{0}".format(10))  #index number
print("{0} {1}".format(20,25))
print("{1} {0}".format(20,25))
print("{num1} {num2}".format(num1=11,num2=12))
print("{num2} {num1}".format(num1=11,num2=12))



